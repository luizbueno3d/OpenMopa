"""Layer model: per-layer operation type and laser parameters.

The UI keeps an ordered list of layers; every imported object carries a
`layer_id`. The planner groups paths by layer and respects layer order.
Disabled and frame-only layers are visible but never emit. Raster layers
emit hatch lines generated by `mopa_luiz.raster` from their closed input
shapes.
"""

from __future__ import annotations

import math
from dataclasses import asdict, dataclass, field
from typing import Iterable

from .cli import MarkConfig
from .safety import validate_layer_settings


VECTOR_ENGRAVE = "vector_engrave"
VECTOR_CUT = "vector_cut"
RASTER_ENGRAVE = "raster_engrave"
FRAME_ONLY = "frame_only"
DISABLED = "disabled"
OPERATIONS = (VECTOR_ENGRAVE, VECTOR_CUT, RASTER_ENGRAVE, FRAME_ONLY, DISABLED)


@dataclass
class LayerSpec:
    layer_id: str
    name: str
    operation: str
    power_percent: float
    frequency_khz: float
    pulse_width_ns: float
    speed_mm_s: float
    passes: int = 1
    visible: bool = True
    output: bool = True
    color: str = "#26d0a8"
    air_assist: bool = False
    raster_pitch_mm: float = 0.1
    raster_angle_deg: float = 0.0
    raster_angle_step_deg: float = 90.0

    def emits_when_marking(self) -> bool:
        if not self.output:
            return False
        if self.operation in {DISABLED, FRAME_ONLY}:
            return False
        return True

    def to_dict(self) -> dict[str, object]:
        return asdict(self)


